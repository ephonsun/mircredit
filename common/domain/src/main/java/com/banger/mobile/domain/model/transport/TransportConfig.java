package com.banger.mobile.domain.model.transport;

public class TransportConfig {
	private boolean enable;
	private int port;
	
	public boolean getEnable() {
		return enable;
	}
	
	public void setEnable(boolean enable) {
		this.enable = enable;
	}
	
	public int getPort() {
		return port;
	}
	
	public void setPort(int port) {
		this.port = port;
	}
}
